                        <p>Sans serif fonts generally don't make the cut for printed books; they might look clean on
                            screens, but they lack the refined touch that serif fonts bring to the page.</p>
                        <p>And don't forget about font size! While 7 pts is the bare minimum, it's safer to go with
                            something closer to 9 or 10 pts to ensure readability.</p>
                        <h3 id="sub-9">Text Alignment</h3>
                        <p>Fully justified alignment is the go-to for both printed books and ebooks. If you're after a
                            more relaxed vibe, you might consider left-aligned or "ragged" right alignment, but keep in
                            mind that this isn't typically seen as best practice. Justified alignment offers a crisp,
                            organized look, but it does take some extra effort. If you spot a word stretching awkwardly
                            across lines, you'll need to hyphenate it to keep the line lengths consistent. It's a bit
                            tedious, but the polished result is well worth it.</p>
                        <h3 id="sub-10">Page Layout</h3>
                        <p>If you've been implementing formatting tips as you read, you're about halfway done. Now,
                            let's shift focus to refining your page layout for both print and digital formats. Pay
                            special attention to these three aspects:</p>
                        <h3 id="sub-11">Trim Size</h3>
                        <p>Trim size—basically, your book's height and width—is vital for print publishing. The standard
                            US paperback size is 6 by 9 inches, suitable for general fiction. However, the ideal trim
                            size varies by genre. Thrillers often use 5.25 by 8 inches, while general non-fiction
                            prefers 5.5 by 8.5 inches. Self-help and inspirational books typically measure around 5 by 8
                            inches. More visual books, like children's literature, usually go larger, commonly 8 by 8
                            inches.</p>
                        <p>For context, a standard A4 sheet is 8.27 by 11.7 inches. Adjust your document to match your
                            chosen trim size to simplify printing and ensure your ebook displays correctly.</p>
                        <h3 id="sub-12">Margins</h3>
                        <p>Margins are essential to prevent your text from being cut off during printing, hence the
                            empty space framing the text in published books. Each page features four types of margins:
                            inside (or gutter), outside, top, and bottom.</p>
                        <p>Outside, top, and bottom margins should be at least 0.25 inches, though you might consider
                            expanding the top and bottom margins to accommodate page numbers (we'll cover that later).
                            Inside margins can range from 0.375 to 0.875 inches, depending on your page count and spine
                            thickness.</p>
                        <h3 id="sub-13">Bleed</h3>
                        <p>If your book includes printed images, you'll need to consider bleed. Unlike text, images
                            shouldn't have margins; they should stretch edge-to-edge, leaving no white space around them
                            in the final printed book. To ensure this, add an extra 0.125 inches to each margin to
                            account for bleed.</p>
                        <h3 id="sub-14">Titles, Headers, and Footers</h3>
                        <p>You're getting close to the finish line. With your fonts set, pages organized, and text
                            formatted for readability, it's time to focus on two more crucial elements: how your chapter
                            headings appear and how you handle running headers and footers.</p>
                        <h3 id="sub-15">Chapter Titles</h3>
                        <p>First off, your first chapter title should always appear on the right-hand page. Leave some
                            space—about a third of the way down the page—between the title and the text, and choose a
                            different font and size for your heading. While it's wise to keep the body text font simple,
                            feel free to be a bit more creative with chapter titles. A smart choice is to use your
                            book's title font to maintain thematic consistency.</p>
                        <p>Chapter openings sometimes get unique formatting compared to the rest of the text. They don't
                            require first-line indents, and you can capitalize the first word and the chapter heading.
                            If you're giving your chapters names, think about adding a line between the chapter number
                            and the title. These small touches help readers easily navigate different sections of your
                            book.</p>
                        <h3 id="sub-16">Running Headers and Footers</h3>
                        <p>To help readers easily navigate your book, running headers and footers are key. Running
                            headers might include the book's title, chapter title, author name, page number, or a mix of
                            these, with the book title, page number, and chapter title aligned to the left, center, and
                            right, respectively. Alternatively, you can place the page number in the footer and reserve
                            the headers for other information. Whichever layout you choose, be sure to remove running
                            headers when a new chapter begins—this space should be dedicated to the chapter title or
                            number.</p>
