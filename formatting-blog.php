                        <h3 id="sub-7">Font Type and Size</h3>
                        <p>For ebooks, font choice isn't a huge issue since most apps let readers adjust it to their
                            liking. But for printed books, your font selection is crucial. Opt for a serif font like
                            Garamond or Centaur to give your book a polished, literary feel.</p>
                        <h3 id="sub-8">Popular Serif Fonts for Books</h3>
                        <p><img src="img/formatting-blog-5.webp" alt="proofreading and book editing a manuscript">
