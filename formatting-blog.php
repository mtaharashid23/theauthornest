                        <p>When you're in the thick of writing, feel free to use whatever font you like. Standard
                            options like Calibri or Arial work well across most word processors, so sticking with them
                            until your draft is done makes sense. The same goes for alignment—you're focused on getting
                            your ideas down, so the finer details can wait.</p>
                        <p>However, when it's time to prepare your manuscript for publishing, you'll want to put some
                            thought into your font choices and text alignment. Here's what you should keep in mind</p>
